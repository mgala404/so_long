#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	*x;

	x = (char *) s;
	while (*x != '\0')
	{
		if (*x == c)
		{
			return (x);
		}
		x++;
	}
	if (*x == c)
		return (x);
	return (0);
}
