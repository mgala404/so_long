#include "libft.h"

char	*ft_strrchr(const char *str, int c)
{
	char	*x;
	int		y;

	x = (char *) str;
	y = ft_strlen(x);
	while (y >= 0)
	{
		if (x[y] == c)
		{
			return (&x[y]);
		}
		y--;
	}
	return (0);
}
