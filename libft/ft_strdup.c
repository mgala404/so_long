#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*d;
	int		l;
	int		x;

	x = 0;
	l = ft_strlen(s);
	d = malloc(sizeof(char) *(l + 1));
	while (x < l)
	{
		d[x] = s[x];
		x++;
	}
	d[x] = '\0';
	return (d);
}
