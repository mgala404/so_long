#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	**k;

	k = lst;
	if (k != 0 && *k != 0)
	{
		ft_lstclear(&(*k)-> next, del);
		ft_lstdelone(*k, del);
		*k = 0;
	}
}
