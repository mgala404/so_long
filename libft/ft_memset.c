#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	size_t			a;
	char			*k;

	a = 0;
	k = (char *)s;
	while (a < n)
	{
		k[a] = c;
		a++;
	}
	return (k);
}
