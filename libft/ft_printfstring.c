#include "libft.h"

int	ft_printfstring(char *frase)
{
	int	j;

	j = 0;
	if (!frase)
	{
		write(1, "(null)", 6);
		return (6);
	}
	while (frase[j] != '\0')
	{
		write(1, &frase[j], 1);
		j++;
	}
	return (j);
}
