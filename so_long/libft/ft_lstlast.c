#include "libft.h"

t_list	*ft_lstlast(t_list *lst)
{
	t_list	*k;

	k = lst;
	if (lst)
	{
		while (k-> next != NULL)
		{
			k = k -> next;
		}
		return (k);
	}
	else
		return (0);
}
